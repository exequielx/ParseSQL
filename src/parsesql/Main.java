    private void reemplazar() {
        String texto = ta_principal.getText().trim().replace('"', '\'').replace('\n', ',');
        if (texto.endsWith(",")) {
            texto = texto.substring(0, texto.length() - 1);
        }
        this.ta_principal.setText(insertarSaltos(texto));
    }
    private void btn_magiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_magiaActionPerformed
        pegarDesdePortapapeles();
        reemplazar();
        copiarAlPortapapeles(ta_principal.getText());
    }//GEN-LAST:event_btn_magiaActionPerformed

    private void limpiar() {
        this.ta_principal.setText("");
    }

    private void pegarDesdePortapapeles() {
        Clipboard c = Toolkit.getDefaultToolkit().getSystemClipboard();
        Transferable t = c.getContents(this);
        if (t == null) {
            return;
        }
        try {
            ta_principal.setText((String) t.getTransferData(DataFlavor.stringFlavor));
        } catch (Exception e) {
            this.ta_principal.setText(e.getMessage());
        }
    }

    private void copiarAlPortapapeles(String s) {
        StringSelection stringSelection = new StringSelection(s);
        Clipboard clpbrd = Toolkit.getDefaultToolkit().getSystemClipboard();
        clpbrd.setContents(stringSelection, null);
    }

