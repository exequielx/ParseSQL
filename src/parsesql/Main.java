    private String insertarSaltos(String texto) {
        int veces = 0;
        int salto = Integer.parseInt(jt_enter.getText().trim());
        String resultado = "";
        if (salto < texto.length()) {
            for (int i = 0; i < texto.length(); i++) {
                resultado += texto.charAt(i);
                if (texto.charAt(i) == ',') {
                    veces++;
                    if (veces == salto && texto.length() - 1 > i) {
                        veces = 0;
                        resultado += "\n";
                    }
                }
            }
            return resultado;
        } else {
            return texto;
        }

    }
